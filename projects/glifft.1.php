              <h2>Reflection</h2>
              <div class="left-align">
                <p>Working in a team of 11 designers was challenging for various reasons. This tremendous opportunity taught me:</p>
                <li>Be firm in the design decisions you believe in, but also realize the product is a combination of everyone's best ideas</li>
                <li>Determine a realistic scope earlier on in the project (quality over quantity), especially when designing the MVP</li>
                <li>UX and UI designers should work together from the beginning to ensure proper communication throughout process</li>
                <li>When working with non-designers, communicate very clearly importance of process and reasons for design decisions</li>
