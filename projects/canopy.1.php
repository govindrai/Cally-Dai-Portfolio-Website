              <h2>Reflection</h2>
              <div class="left-align">
                <p>Working on Canopy taught me how to thrive working in ambiguous circumstances. Being a very early stage product, the founders were very flexible. Although this made them extremely receptive to our ideas, it was challenging optimizing designs for an indistinct product. Ultimately, we learned how to ask for concrete answers to narrow the scope of our solutions.</p>
                <p>Working with clients also taught me how to effectively communicate design decisions. I learned to be careful not to use jargon and to explain only things in ways non-designers can digest. Additionally, it is helpful to limit options as to not overwhelm them with a great number of wireframes and mockups.</p>
