          <h2>Design Process</h2>
          <h3 id="persona-development">Persona Development</h3>
          <p>First, we aimed to create a persona to help guide our design decisions. Soma&nbsp;provided demographic data of their existing audience to serve as a basis for our persona. The data was diverse and broad, so we decided to create two personas to represent the statistics. Click on personas below to see larger images.</p>
